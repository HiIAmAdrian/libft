#include <unistd.h>

void	ft_putendl(char const *s)
{
	int		i;
	char	c;

	i = 0;
	c = '\n';
	if (s)
		while (s[i])
		{
			write(1, &s[i], 1);
			i++;
		}
	write(1, &c, 1);
}
